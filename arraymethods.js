const arr = [1,2,3,4]

//push -- adds an element to the end of arr and returns the length of the new array
console.log(arr.push(10), arr)
// pop -- removes the last element and returns it
console.log(arr.pop(), arr)

//unshift -- Adds elements to the beginning of an array and return the new array length
console.log(arr.unshift(7), arr)
//shift -- removes the first element and returns it
console.log(arr.shift(), arr)

